    private Operaciones getOperador(){
            float Elemento01, Elemento02;
        Elemento01 = Float.parseFloat(txtElemento01.getText());
        Elemento02 = Float.parseFloat(txtElemento02.getText());
        
        Operaciones ope = new Operaciones(Elemento01, Elemento02);
        
        return ope;
    }
    
